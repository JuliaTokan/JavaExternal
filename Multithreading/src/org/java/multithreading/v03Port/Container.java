package org.java.multithreading.v03Port;

public class Container {
    private int num;
    private int weight;

    public Container(int num, int weight) {
        this.num = num;
        this.weight = weight;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
